package datatypesandvariables;

public class variableanddatatypes {

	public static void main(String[] args) {
		//Numeric DataTypes 
		int a=100 ,b=200;
		System.out.println("The value of a is:" +a);
		System.out.println("The value of b is:" +b);
		System.out.println("The Sum of a & b is:"+(a+b));
		
		byte by=125;
		System.out.println("The value of byte is:"+by);
		
		short sh = 32000;
		System.out.println("The value of short is:"+sh);
		
		long lg=132132123L;
		System.out.println("The value of long is:"+lg);
		
		
		//Decimal numbers-float/double 
		 float item_price=15.5f;
		 System.out.println("The value of this:"+item_price);
		 
		 double dl=124.3343343434;
		 System.out.println("The vlaue for dl is:"+dl);
		
		
		//character Datatype
		 char Grade='C';
		 System.out.println("Your Grade is:"+Grade);
		//Strings 
		String Name="Hatum Hussain";
		System.out.println(Name);
		
		//boolean
		boolean qa = true;
		System.out.println(qa);
		 
		 
		 
		
		
		
		
	}

}
